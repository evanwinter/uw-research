                <div class="red-divider-left"></div>

                <h2>Graduate research impacted by collaboration with undergrads</h2> 

                <p>Undergraduate research participation at UW-Madison has consistently provided students with learning opportunities not available to them within the classroom. Undergraduate students involved in various fields of research have reported a plethora of benefits that their lab participation has brought them, but they are not the only ones who benefit from undergraduate research. </p>

                <p>As a graduate student conducting research on non-linear vibrations, Robert Lacayo believes that his research benefits from collaboration with undergrads. </p>

                <p>“If there are more people working on something, obviously things will move along more quickly,” said Lacayo. “Also, with these students working on certain parts of the research, I’m able to focus more on different specific aspects, which increases the quality of the work being done.” </p>

                <p>As facilitators of research labs, professors and graduate students work directly with their undergraduate assistants to gather and analyze information to further discovery. Though they have more experience in their field, facilitator’s work can be enhanced by collaboration with undergraduates. </p>

                <p>“As a graduate student who entered school with clear intentions of doing research for an advisor, it’s very rewarding to work with undergrads who are where I was three years ago,” said Lacayo. “I don’t only teach these students; I learn from them as well because they are curious and have new ideas.”</p>

                <p>Graduate students facilitating research are responsible for not only propelling student research participation, but mentoring students as well. This guidance has the potential to benefit the field of research through encouraging further involvement. </p>

                <p>“Encouraging people’s knowledge about a subject can be overwhelming for them sometimes, especially if this is their first research position,” said Lacayo. “But being able to mentor students and help them become interested in engineering as a future benefits the research in this field because there will be more top-notch scholars in the field when they are done with school.”</p>

                <p>While the undergraduate research experience benefits undergraduates in a variety of ways, the graduate research experience has benefits too. And by combining the two, mutual growth and understanding become possible.  </p>

