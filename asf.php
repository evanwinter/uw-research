                <h2>Research Smorgasbord</h2>
                <h3>UW-Madison Serves Up a Buffet of Varied Research </h3>

                <p>UW-Madison does a lot of research. So much, in fact, it’s hard to keep up with it all. To help you stay abreast — and sound smart at the next cocktail party — here’s a few of the school’s recent standout projects: </p>

                <ul class="talking-points">
                    <li>Providing a better understanding of the Zika virus and finding ways to stop it from spreading</li>
                    <li>Researching children’s emotional development and the relationship between early emotional experience and child psychopathology</li>
                    <li>Examining how disparities in socioeconomic status affect children’s development, and how these effects may be altered by policies and programs, especially early childhood education programs</li>
                    <li>Creating innovative energy harvesting and storage technology by using human motion, such as walking to power mobile electronic devices</li>
                    <li>Discovery of a new fish virus that is a key suspect in a die-off of largemouth bass. This discovery could help protect a critical natural resource and the quality of life in Wisconsin</li>
                    <li>Researching infant music perception, and the relationship between music and language learning</li>
                    <li>Investigations into influenza, including changing how flu vaccines are made by providing faster and more cost-effective methods, that could help us beat back the type of flu viruses that have erupted into deadly pandemics in the past</li>
                    <li>Creating an atlas of 23,000 plant and bacterial proteins to ultimately help inform a strategy for engineering the nitrogen-fixing ability of legumes into other plants, thus answering a challenge in agricultural biotechnology</li>
                    <li>Improving the understanding of the nature and rate of cognitive decline among patients with Alzheimer’s disease, and improved ability to track and anticipate the progression of Alzheimer’s that will help in the development and evaluation of preventative drugs.</li>
                    <li>Strengthening our satellite imagery technology, including inventing the instrumentation to make possible the animated satellite pictures that are staples of TV weather reports</li>
                </ul>
                (Source: UW-Madison University Communications)

                
